// main.js - Vue 应用的入口文件
// 这个文件负责创建 Vue 应用实例，并挂载到 DOM 上

// ============================================
// 1. 引入必要的依赖
// ============================================
import { createApp } from 'vue'      // Vue3 的创建应用函数
import { createPinia } from 'pinia'  // Pinia 状态管理库
import App from './App.vue'          // 根组件
import router from './router'        // 路由配置

// ============================================
// 2. 引入全局样式
// ============================================
import './style.css'

// ============================================
// 3. 创建 Vue 应用实例
// ============================================
// createApp() 创建一个 Vue 应用实例
// 参数是根组件（App.vue）
const app = createApp(App)

// ============================================
// 4. 注册插件
// ============================================
// 使用 Pinia 状态管理
// createPinia() 创建 Pinia 实例
app.use(createPinia())

// 使用 Vue Router 路由
app.use(router)

// ============================================
// 5. 挂载应用
// ============================================
// mount('#app') 将应用挂载到 index.html 中 id="app" 的元素上
app.mount('#app')

// 至此，Vue 应用启动完成！
